template <class T>
void print_array(const T* arr, size_t sz, ostream* os) {
	*os << "{";
	for (int i = 0; i < sz; i++) {
		*os << arr[i];
		if (i < sz-1) {
			*os << ',';
		}
	}
	*os << "}";
}

void PrintTo(const input_t& v, ostream* os) {
	*os << "(";
	print_array(v.x, v.n, os);
	*os << ", ";
	print_array(v.y, v.n, os);
	*os << ", ";
	print_array(v.r, v.n, os);
	*os << ", " << v.n << ", ";
	*os << v.x1 << ", " << v.y1 << ", ";
	*os << v.x2 << ", " << v.y2 << "}";
}

