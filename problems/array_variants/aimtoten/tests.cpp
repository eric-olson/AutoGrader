class input_t {
public:
	int* arr;
	size_t sz;
};

void PrintTo(const input_t& v, ostream* os) {
	*os << "({";
	for (int i = 0; i < v.sz; i++) {
		*os << v.arr[i];
		if (i < v.sz-1) {
			*os << ',';
		}
	}
	*os << "}, " << v.sz << ")";
}

